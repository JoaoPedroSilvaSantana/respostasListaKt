class calculadora(val a: Int, val b: Int){
    
    fun somar(a: Int, b: Int):Int{
        return a + b
    }
    
    fun subtrair(a: Int, b: Int):Int{
        return a - b
    }
    
    fun mutiplicar(a: Int, b: Int):Int{
        return a * b
    }
    
    fun dividir(a: Int, b: Int):Int{
        return a / b
    }
}
